package com.njust.helper.model;

public class CourseLoc {
    private String id, classroom, week1, week2;
    private int sec1, sec2, day;

    public String getId() {
        return id;
    }

    public String getClassroom() {
        return classroom;
    }

    public String getWeek1() {
        return week1;
    }

    public String getWeek2() {
        return week2;
    }

    public int getSec1() {
        return sec1;
    }

    public int getSec2() {
        return sec2;
    }

    public int getDay() {
        return day;
    }
}
